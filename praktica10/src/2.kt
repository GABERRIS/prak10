fun pyramid(n: Int) {
    // Проверка на корректность ввода.
    if (n <= 0) {
        println("Число уровней пирамиды должно быть положительным.")
        return
    }

    // Цикл по уровням пирамиды.
    for (i in 1..n) {
        // Вычисление количества пробелов слева.
        val spaces = n - i
        // Вывод пробелов слева.
        repeat(spaces) { print(" ") }
        // Вывод символов "#" для уровня.
        repeat(2 * i - 1) { print("#") }
        // Вывод пробелов справа.
        repeat(spaces) { print(" ") }
        // Переход на новую строку после вывода уровня.
        println()
    }
}

fun main() {
    // Примеры вызова функции.
    pyramid(1)
    println() // Разделитель между примерами.
    pyramid(2)
    println()
    pyramid(4)
}